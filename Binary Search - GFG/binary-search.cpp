class Solution {
  public:
    int binarysearch(int arr[], int n, int k) {
        // code here
        int bin=binary_search(arr,arr+n,k);
        if(!bin) return -1;
        auto low=lower_bound(arr,arr+n,k);
        int l=low-arr;
        return l;
    }
};

